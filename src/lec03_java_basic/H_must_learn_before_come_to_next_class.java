package lec03_java_basic;

public class H_must_learn_before_come_to_next_class { // Start of the class body
	// inside curly braces, This is called class body
	// What a class body contains?	
	// First: Class body contain variables
	// Those variables can be either declared (no value assigned) or initialized (value assigned) 

	// here, public is access modifier, String is the type of the variable, myName is the name of the String type variable
	public String myName; // Variable is declared, no value assigned
	
	// here, public is access modifier, String is the type of the variable, country_Name is the name of the variable
	public String country_name = "USA"; // Variable is initialized, a value is assigned
	
	// variables name start with lower case and follow camel case and snake case feature
	
	// Second: Class body also contains Constructor after variables.
	// Those Constructor can be either declared or initialized
	
	// Third: Class body also contains methods
	// Inside the class, Those methods can be either implemented or initialized. (very important info)
	// Inside the class, method can't be declared  (very important info)
	
	// After method, main method can come if we create
	public static void main(String[] args) { // Start of the main method body
		// main method body
		System.out.println("Print");
	} // End of the main method body
	
	
	
	
} // End of the class body
